SUBROUTINE REPLICATE2 (KOFF, P)

INTEGER :: KOFF
REAL (KIND=JPRB)    :: P (:,:)

INTEGER :: I, J

DO I = KOFF+1, SIZE (P, 1)
  J = 1 + MODULO (I - 1, KOFF)
  P (I, :) = P (J, :)
ENDDO

END SUBROUTINE

SUBROUTINE REPLICATE1 (KOFF, P)

INTEGER :: KOFF
REAL (KIND=JPRB)    :: P (:)

INTEGER :: I, J

DO I = KOFF+1, SIZE (P, 1)
  J = 1 + MODULO (I - 1, KOFF)
  P (I) = P (J)
ENDDO

END SUBROUTINE

SUBROUTINE NPROMIZE2 (KPROMA, PI, PO)

INTEGER :: KPROMA
REAL (KIND=JPRB),  INTENT (IN)  :: PI (:,:) 
REAL (KIND=JPRB),  INTENT (OUT) :: PO (:,:)

INTEGER :: I, J, IGPBLK, IGPTOT, IGP, JLON, JIDIA, JFDIA

IF (SIZE (PI, 2) /= 1) STOP 1

IGPTOT = SIZE (PI, 1)
IGPBLK = 1 + (IGPTOT-1) / KPROMA

DO IGP = 1, IGPTOT, KPROMA
  IBL = 1 + (IGP - 1) / KPROMA
  JIDIA = 1
  JFDIA = MIN (KPROMA, IGPTOT - (IBL - 1) * KPROMA)

  DO JLON = JIDIA, JFDIA
    PO (JLON, IBL) = PI (IGP + (JLON - 1), 1)
  ENDDO

  DO JLON = JFDIA+1, KPROMA
    PO (JLON, IBL) = PO (JFDIA, IBL)
  ENDDO

ENDDO

END SUBROUTINE

SUBROUTINE NPROMIZE3 (KPROMA, PI, PO)

INTEGER :: KPROMA
REAL (KIND=JPRB),  INTENT (IN)  :: PI (:,:,:) 
REAL (KIND=JPRB),  INTENT (OUT) :: PO (:,:,:)

INTEGER :: I, J, IGPBLK, IGPTOT, IGP, JLON, JIDIA, JFDIA

IF (SIZE (PI, 3) /= 1) STOP 1

IGPTOT = SIZE (PI, 1)
IGPBLK = 1 + (IGPTOT-1) / KPROMA

DO IGP = 1, IGPTOT, KPROMA
  IBL = 1 + (IGP - 1) / KPROMA
  JIDIA = 1
  JFDIA = MIN (KPROMA, IGPTOT - (IBL - 1) * KPROMA)

  DO JLON = JIDIA, JFDIA
    PO (JLON, :, IBL) = PI (IGP + (JLON - 1), :, 1)
  ENDDO

  DO JLON = JFDIA+1, KPROMA
    PO (JLON, :, IBL) = PI (JFDIA, :, IBL)
  ENDDO

ENDDO

END SUBROUTINE

! reverse affectation
SUBROUTINE UNPROMIZE3 (KPROMA, PI, PO)

INTEGER :: KPROMA
REAL (KIND=JPRB),  INTENT (OUT)  :: PI (:,:,:) 
REAL (KIND=JPRB),  INTENT (IN) :: PO (:,:,:)

INTEGER :: I, J, IGPBLK, IGPTOT, IGP, JLON, JIDIA, JFDIA

IF (SIZE (PI, 3) /= 1) THEN
  write(*,*) "fail !"
  STOP 1
END IF

IGPTOT = SIZE (PI, 1)
IGPBLK = 1 + (IGPTOT-1) / KPROMA

DO IGP = 1, IGPTOT, KPROMA
  IBL = 1 + (IGP - 1) / KPROMA
  JIDIA = 1
  JFDIA = MIN (KPROMA, IGPTOT - (IBL - 1) * KPROMA)

  DO JLON = JIDIA, JFDIA
    PI (IGP + (JLON - 1), :, 1) = PO (JLON, :, IBL)
  ENDDO

  DO JLON = JFDIA+1, KPROMA
    PI (JFDIA, :, IBL) = PO (JLON, :, IBL)
  ENDDO

ENDDO

END SUBROUTINE


